    <h3><a name="1skyrius1poskyris">1. 1. SISTEMOS APRAŠYMAS</a></h3>
    VAT IS - VAT Invoice System yra PVM sąskaitų faktūrų išrašymo sistema. Kadangi VMI griežtai reglamentuoja PVM apskaitą bei PVM sąskaitų faktūrų išrašymą ir rekvizitus, apskaičiuotą PVM, ši sistema palengvinti ir padeda kontroliuoti išrašančių PVM sąskaitas faktūras asmenų darbą, kad būtų išvengta klaidų. Pagrindiniai sistemos funkcionalumai:
    <ul>
      <li>galimybė registruoti įmonės pirkėjus bei tiekėjus, darbuotojus, valstybės institucijas;</li>
      <li>galimybė peržiūrėti užregistruotus kontrahentus, tvarkyti jų duomenis;</li>
      <li>galimybė registruoti PVM sąskaitas faktūras, kreditines sąskaitas;</li>
      <li>galimybė formuoti PDF dokumentus išrašytoms sąskaitoms;</li>
      <li>galimybė ieškoti ir filtruoti sąskaitų sąrašą pagal nustatytus kriterijus;</li>
      <li>galimybė įvesti parduodamą produkciją (prekes, paslaugas), ją peržiūrėti ir tvarkyti.</li>
    </ul>
    Sistema yra suskirstyta į klientų, PVM sąskaitų ir produktų dalis. <br />
